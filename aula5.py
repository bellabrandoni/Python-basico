#Tipo de dado bool (Boolean)
#Ao questionar algo em um programa, so existem duas respostas possiveis:
#sim(true) ou nao(false).
#Existem vários operadoes para "questionar".
#Dentre eles o == que é um operador lógico que confere se um valor é igual a outro valor e se eles são do mesmo tipo.  

print(10 == 10) #Sim ->True
print(10 == 11) #Nao -> False
print(type(10 == 10)) #->bool = retorna o tipo de dado booleano 